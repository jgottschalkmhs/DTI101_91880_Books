            <p>'Saturn Run' begins with an intern discovering a possible alien craft decelerating towards Saturn. This sparks a 'race to Saturn' with America trying to beat China to the ringed planet (and any possible alien technology).</p>

            <p>The story is fast moving and compelling. The characters are well developed and as a reader I became quite invested in their personal stories. Whilst the book took quite a while to read, the journey was worth while. The Physics underlying the novel has been well researched and this helps to make the story both interesting and believable.</p>

            <p>The tight plot kept me engaged throughout and the author also used the story as a way of exploring politics, science and ultimately what it means to be human. It was a relief to see that the authors shied away from some of the cliches typically found in this genre and the result was a well crafted novel that appeals to a wide audience.</p>
              
